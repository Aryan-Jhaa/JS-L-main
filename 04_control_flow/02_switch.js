const month = 2;
let monthName = undefined;
switch (month) {
    case 1:
        monthName = "January";
        console.log(monthName);
        break;
    case 2:
        monthName = "February";
        console.log(monthName);
        break;
    case 3:
        monthName = "March";
        console.log(monthName);
        break;
    default:
        console.log("Default case for month value above 3");
        break;
}